function renderReviews(reviews) {
  const container = document.getElementById('reviews-container');

  if (!reviews.length) {
    container.innerHTML = '<div class="col-12"><p class="text-center">No reviews available.</p></div>';
    return;
  }

  reviews.forEach((review, index) => {
    const col = document.createElement('div');
    col.className = 'col-12 col-md-6 col-lg-4 mb-4';

    const stars = getStars(review.overallRating || 0);
    const reviewerName = (review.name && review.name.includes('(A)')) ? 'Anonymous' : (review.name || 'Anonymous');

    const year = review.year || '';
    const difficulty = review.difficulty ?? 'N/A';
    const grading = review.gradingHarshness ?? 'N/A';
    const gradingComment = review.gradingComment || 'N/A';
    const attendance = review.attendancePolicy || 'N/A';
    const tag = review.tag || 'N/A';
    const fullReview = (review.overallReview || 'No detailed review provided.').replace(/\n/g, '<br>');

    col.innerHTML = `
      <div class="card h-100 review-card shadow-sm" id="review-card-${index}">
        <div class="card-body">
          <div class="review-summary">
            <h5 class="card-title mb-2">${review.courseCode} - ${review.courseName}</h5>
            <div class="review-meta mb-1">
              <small class="text-muted">${review.instructor} • ${review.semesterSeason} • ${year}</small>
            </div>
            <div class="stars mb-2">${stars}</div>
            <div class="mb-2">
              <span class="badge bg-primary me-1">Difficulty: ${difficulty}/5</span>
              <span class="badge bg-secondary">Grading: ${grading}/5</span>
            </div>
            <small class="text-muted">Reviewed by: ${reviewerName}</small><br>
            <span class="toggle-text text-secondary mt-2 d-inline-block">Click to view full review</span>
          </div>

          <div class="review-details mt-3">
            <p><strong>Grading Comment:</strong> ${gradingComment}</p>
            <p><strong>Attendance Policy:</strong> ${attendance}</p>
            <p><strong>Tag:</strong> ${tag}</p>
            <p><strong>Overall Review:</strong><br>${fullReview}</p>
          </div>
        </div>
      </div>
    `;

    col.querySelector('.review-card').addEventListener('click', function () {
      this.classList.toggle('expanded');
    });

    container.appendChild(col);
  });
}


