function renderTeam(members) {
  const container = document.getElementById('team-container');

  members.forEach(member => {
    const col = document.createElement('div');
    col.className = 'col-12 col-sm-6 col-md-4 col-lg-3 mb-4';

    const isCoordinator = member.isCoordinator;
    const cardClass = isCoordinator ? 'team-card coordinator-card' : 'team-card';

    const coordinatorBadge = isCoordinator
      ? `<div class="coordinator-badge">Coordinator</div>`
      : '';

    const fallbackImage = `https://placehold.co/400x300/2c3e50/ffffff?text=${member.name.charAt(0)}`;

    col.innerHTML = `
      <div class="card ${cardClass} h-100 position-relative shadow-sm">
        ${coordinatorBadge}
        <img 
          src="${member.image}" 
          class="card-img-top img-fluid rounded-top" 
          alt="${member.name}" 
          onerror="this.onerror=null;this.src='${fallbackImage}'" 
        />
        <div class="card-body text-center">
          <h5 class="card-title">${member.name}</h5>
          <p class="card-text text-muted">${member.role}</p>
        </div>
      </div>
    `;

    container.appendChild(col);
  });
}


function getStars(rating) {
  const r = Math.round(Number(rating) || 0);
  return '★'.repeat(r) + '☆'.repeat(5 - r);
} 